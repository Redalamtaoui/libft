#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	x;

	x = 0;
	if (fd < 0)
		return ;
	if (!s)
		return ;
	while (s[x])
	{
		ft_putchar_fd(s[x], fd);
		x++;
	}
}
